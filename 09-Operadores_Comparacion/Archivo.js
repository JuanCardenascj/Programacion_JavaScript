/*Operadores de Comparacion*/

//Declaramos dos varibales
let valor1, valor2; //Crea las variables y no le asigna valor
valor1 = 20; //Declara la variable la asgina valor
valor2 = 10; //Declara la variable la asgina valor

/*Para operador de compracion Mayor */
document.write(valor1 > valor2); //Compara si las variables cual es mayor o menor.
document.write('<br>') //Salto de linea

/*Para operador de compracion Menor */
document.write(valor1 < valor2); //Compara si las variables cual es menor o mayor.
document.write('<br>') //Salto de linea

/*Para operador de compracion Mayor o igual*/
document.write(valor1 >= valor2); //Compara si las variables es mayor o igual.
document.write('<br>') //Salto de linea

/*Para operador de compracion Menor o igual*/
document.write(valor1 <= valor2); //Compara si las variables es menor o igual.
document.write('<br>') //Salto de linea

/*Para operador de compracion de igualdad*/
document.write(valor1 == valor2); //Compara si las variables son igual a la otra.
document.write('<br>') //Salto de linea

/*Para operador de compracion Diferente o Diferencia*/
document.write(valor1 != valor2); //Compara si las variables son diferente de la otra.
document.write('<br>') //Salto de linea

