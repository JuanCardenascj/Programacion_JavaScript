//Roud ( Redondea precio ) a siguiente entero en decimal .5
var precio = Math.round(399.53); //Variable reservada
document.write('Precio redondeado: ', precio);
document.write('<br>'); //Salto de linea

//Ceil ( Redondea precio ) sin importar el decimal siempre lo va rededondear hacia arriba
var precio = Math.ceil(299.9); //Variable reservada
document.write('Precio redondeado: ', precio);
document.write('<br>'); //Salto de linea

//Floor ( Redondea precio) sin importar el decimal siempre lo va redondear hacia abajo
var precio = Math.floor(540.9); //Variable reservada
document.write('Precio redondeado: ', precio);
document.write('<br>'); //Salto de linea

//Sin - Calcula el Seno
var seno = Math.sin(45); //Variable reservada
document.write('Seno de 45: ', seno);
document.write('<br>'); //Salto de linea

//Exp - Calcula el exponencial de un numero
var expo = Math.exp(2); //Variable reservada
document.write('Exponencial de 2: ', expo);
document.write('<br>'); //Salto de linea

//Log - Logaritmo de un numero
var Logaritmo = Math.log(10); //Variable reservada
document.write('Logaritmo de 10: ', Logaritmo);
document.write('<br>'); //Salto de linea

//Abs - Valor absoluto de un numero
var absoluto = Math.abs(-10); //Variable reservada
document.write('El valor absoluto de -10; ', absoluto);
document.write('<br>'); //Salto de linea

//Max - Calcular el valor maximo de una variable
var maximo = Math.max(10, 50, 600, 1, 6); //Variable reservada
document.write('Mayor valor: ', maximo);
document.write('<br>'); //Salto de linea

//Min - Calcula el valor minimo de una variable
var minimo = Math.min(10, 50, 600, 1, 8); //Variable Reservada
document.write('Minimo valor: ', minimo);
document.write('<br>'); //Salto de linea

//Random - Traer el pantalla valor aleatorio
var aleatorio = Math.random(); //Variable reservada
document.write('Valor aleatorio: ', aleatorio);
document.write('<br>'); //Salto de linea

//Sqrt - Raiz cuadrada de un numero
var valor = Math.sqrt(81); //Palabra reservada
document.write('La raiz de 81: '.valor);
document.write('<br>'); //Salto de linea

//Pow - Exponenete de un numero
var exponente = Math.pow(4, 2); //Variable reservada
document.write('Valor de 4 a la 2: ', exponente);
document.write('<br>'); //Salto de linea
